#include "libft.h"

char *ft_strncat(char *restrict s1, const char *restrict s2, size_t n)
{
	int i;
	int k;

	i = 0;
	k = 0;
	while (s1[i] != '\0')
		i++; 
	while (s2[k] != '\0' && n > 0)
	{
		s1[i] = s2[k];
		i++;
		k++;
		n--;
	}
	s1[i] = '\0';
	return (s1);
}
